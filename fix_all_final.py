def random_date():
    """Генерує випадкову дату між 10.10.2025 та 06.12.2025"""
    start = datetime(2025, 10, 10)
    end = datetime(2025, 12, 6)
    delta = end - start
    random_days = random.randint(0, delta.days)
    return (start + timedelta(days=random_days)).strftime("%d.%m.%Y")

